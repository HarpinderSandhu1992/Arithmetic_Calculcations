function add(value1, value2) {
  return value1 + value2;
}
function sub(value1, value2) {
  return value1 - value2;
}
function multiply(value1, value2) {
  return value1 * value2;
}
function divide(value1, value2) {
  return value1 / value2;
}

module.exports = {
  add,
  sub,
  multiply,
  divide,
};
